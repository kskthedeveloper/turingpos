    private int getTotalPrice() {
        int totalPrice = 0;
        for(int id: transItemMap.keySet()) {
            TransactionItem tranItem = transItemMap.get(id);
            totalPrice += tranItem.getQuantity() * tranItem.getPrice();
        }
        return totalPrice;
    }

