    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String strQty = txtQty.getText();

        if(strQty != "" && this.item != null) {
          
            int quantity = Integer.parseInt(strQty);

            TransactionItem transItem = new TransactionItem(item, quantity);

            this.transItemTableModel.insertRow(this.transItemTableModel.getRowCount(), new Object[]{this.item.getId(), this.item.getItemCode(), this.item.getName(), this.item.getItemCategory().getName(), this.item.getPrice(), quantity, (this.item.getPrice() * quantity)});
            this.txtItemCode.setText("");
            this.txtQty.setText("0");
            this.lblPrice.setText("");
            this.lblCategory.setText("");

            this.transItemMap.put(this.item.getId(), transItem);
            this.lblTotal.setText(getTotalPrice()+"");
            this.item = null;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(tblPurchaseItem.getSelectedRow() >= 0) {
            String itemId = String.valueOf(transItemTableModel.getValueAt(tblPurchaseItem.getSelectedRow(), 0));
            this.transItemTableModel.removeRow(tblPurchaseItem.getSelectedRow());
            this.transItemMap.remove(Integer.parseInt(itemId));
            this.lblTotal.setText(getTotalPrice()+"");
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        User user = this.userService.getUser();

        Date date = Date.valueOf(txtPurchaseDate.getText());
        List<TransactionItem> itemList = new ArrayList<>();

        for(int id: transItemMap.keySet()) {
            itemList.add(transItemMap.get(id));
        }
        purchaseService.addTransaction(date, user, itemList);
    }//GEN-LAST:event_jButton3ActionPerformed

    
    
    private boolean isDuplicateCode(Item item) {
        return this.transItemMap.containsKey(item.getId());
    }
    
