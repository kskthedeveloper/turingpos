    private void txtItemCodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtItemCodeKeyPressed
        // TODO add your handling code here:
        //        System.out.println("Key Pressed");
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            // Search by code

            this.item = itemService.findByCode(txtItemCode.getText());
            if(this.item != null) {
                if(isDuplicateCode(item)) {
                    JOptionPane.showMessageDialog(this, "Duplicated code");
                    txtItemCode.setText("");
                } else {

                    lblPrice.setText(item.getPrice()+"");
                    lblCategory.setText(item.getItemCategory().getName());
                }
            }
        }
    }//GEN-LAST:event_txtItemCodeKeyPressed

